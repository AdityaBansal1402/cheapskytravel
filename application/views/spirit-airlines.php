<h3>Introduction to Spirit Airlines</h3>
<p>A well-known, inexpensive airline that has been operating for years is Spirit Airlines. Spirit Airlines is based in Miramar, Florida, and has as its goal to offer you the best deal possible. To start with, they have their trademark Ultra Low Fare. Spirit flies to more than 60 locations around the United States, Latin America, as well as the Caribbean with around 500 flights daily. Spirit Airlines' business model is based on the premise that by cutting out all of the extras that most airlines charge for, you'll be left with a reasonable ticket and the freedom to spend only what you wish.</p>
<h3>Baggage policy</h3>
<p>As a low-cost carrier, Spirit does not offer a lot of baggage allowance. A single compact personal item, no more than 18" x 14" x 8," is permitted per passenger. Excess fees apply to any additional luggage.</p>
<p>The luggage costs charged by Spirit Airlines are not listed online. However, you may estimate your potential additional luggage fees using their online calculator. You only need to enter your travel information to find out how much your additional carry-ons and checked luggage will cost you.</p>
<p>Substantially until the gate, you can add luggage, but be aware that the cost will up as you get nearer to departure. Book your luggage at the same time as your flight to save the most money!</p>
