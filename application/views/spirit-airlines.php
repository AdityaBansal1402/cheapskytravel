<h3>Entertainment and facilities</h3>
<p>All meals and drinks are the responsibility of Spirit Airlines. Only credit or debit cards are accepted for purchases of snacks and beverages. You can choose from juice, soda, coffee, beer, wine, even cocktails.</p>
<p>There are no televisions or other forms of in-flight entertainment aboard Spirit Airlines flights. However, they now provide onboard WiFi. On a higher-end carrier, there are no amenity packs, pillows, blankets, outlets, or other comforts.</p>
<h3>Boarding process</h3>
<p>Travelers frequently get the feeling that boarding a cheap flight is free for all. Insanity is intentional; there is a reason for it. Pre-boarding, family boarding and four numbered zones are all features of Spirit Airlines. Spirit Carriers offers one of the easiest and most convenient boarding procedures among budget airlines.</p>
<h3>Reimbursement for delayed baggage</h3>
<p>For travelers whose bags were delayed, Spirit Airlines will cover reasonable interim costs. Only reasonable personal purchases made as a consequence of the delay, including clothing and toiletries, are eligible for interim expenditures. To be compensated, you must preserve the original receipts; duplicates are not accepted.</p>
