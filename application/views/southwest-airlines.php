			<h3>Southwest doesn't manipulate prices in any way</h3>
<p>Southwest is a low-cost airline, although it isn't a price-first discounter, therefore you must know that before you consider making a reservation. You will actually pay that amount, which includes the carry-on plus up to two checked baggage when you use Southwest's published pricing.</p>
<p>The airline doesn't offer business or first class. You may spend for early-bird check-in, which will automatically check you in 24 hours prior to your trip, or Business Select, which will allow you to board your flight among the first 15 people who do not have preboard status.</p>
<p>Although the airline does provide more expensive prices with greater flexibility for changing or canceling reservations, the base fare is inclusive. No additional fees will be applied by Southwest for luggage, seat assignments, or rows with more room for the legs.</p>
<h3>Southwest Has an Extraordinary Boarding Procedure</h3>
<p>Customers can check in for a flight 24 hours prior to departure, according to the airline. A letter (A, B, or C) plus the number (1 through 60) is assigned to you as you check in, which you must do as early as you can. As it's time to board, travelers line up in numerical sequence, with the A group going first.</p>
<p>A Southwest flight has no reserved seating. If a passenger does not have pre-boarding authorization, they board as per their letter and number. If they have no spot in the A group, families traveling with young kids can board in the A and B groups.</p>
           
