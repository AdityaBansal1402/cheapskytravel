           <h3>What is the baggage policy at Frontier?</h3>
<p>Before you head to the airport, familiarize yourself with Frontier's luggage policies and costs if you intend to fly with them. Otherwise, you run the risk of spending more money than you intended.</p>
<p>Based on the type of ticket you've purchased and also the dimensions of your luggage, Frontier Airlines offers a few different limits. Their "Classic" ticket, for instance, only permits one personal item, like a handbag or laptop bag. This cannot go over 14 "8" long, 18" wide, and tall.</p>
<p>You will need to pay an additional fee if you wish to have a carry-on bag. No carry-on bag may be larger than 24" tall, 10" broad, and 16" long.</p>
<h3>Frontier Airlines' Cancellation Policy</h3>
<p>On paper, Frontier Airlines' cancellation policy appears to be reasonable. If one cancels more than seven days before the scheduled departure, they will get a full refund. You will have to forfeit the full cost of the ticket if you cancel fewer than seven days before the scheduled departure. Additionally, up to 24 hours following purchase, Frontier offers full refunds to all clients who cancel their tickets.</p>
