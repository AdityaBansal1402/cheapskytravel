<p>Air France allows passengers to carry hand luggage, personal items, as well as checked luggage for free based on flight type, ticket category, destination, and membership level. In this section, we go through each allowed baggage regulation and guide you through the <strong>Air France baggage allowance</strong> in layman’s terms.&nbsp;</p>
<h3>Carry-on or Hand baggage</h3>
<p>Air France allows passengers traveling in the Economy cabin to take one cabin luggage and the same number of personal items without adding any additional baggage charges. On the other hand, a passenger flying in <strong>Premium Economy, Business, or La Première cabins</strong> can carry two cabin baggage in addition to one personal item.&nbsp;</p>
<ul>
<li>The weight of each baggage should not exceed <strong>12</strong> kg for hand luggage.</li>
<li>Passengers with <strong>Premium Economy, Business, or La Première</strong> cabin tickets need to keep the total weight of allowed items within <strong>18</strong> kg.&nbsp;</li>
<li>Dimensions of each carry-on bag must not exceed <strong>55 x 35 x 25</strong> cm including wheels, pockets, and handles. However, the personal item needs to be within <strong>40 x 30 x 15</strong> cm.&nbsp;</li>
<li>The <strong>Air France carry-on baggage </strong>regulation specifies passengers need to place their cabin luggage in the overhead cabin and personal items under the front seat.</li>
</ul>
<p>Note: On some routes, the airline will allow one added luggage to passengers traveling with an infant or lap child.</p>
