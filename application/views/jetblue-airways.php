<h3>Baggage policy</h3>
<p>You are given one free personal item and one complimentary carry-on bag when you fly with JetBlue. When traveling, you can add two pieces of checked baggage. Your checked bags might be free, based on the ticket category you choose, your loyalty program standing, and whether you have a JetBlue credit card.</p>
