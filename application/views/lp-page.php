<p><strong>Policies regarding flight switch and cancellation</strong></p>
<p>Flight adjustments or cancellations cost $0 if made 60 days or more before departure, $49 if made 59 to seven days before departure, and $79 if made six days or less before departure, which covers same-day alterations and cancels. Some who switch flights must make up the difference when the new flight is costlier; however, no residual credit would be given if the flight costs less.</p>

<p>All who withdraw will receive a travel credit equal to the ticket amount less the cancellation charge. People who purchase The Works bundle will receive a complete refund; anybody can receive a refund on their reservation if it is canceled before 24 hours of purchasing if the trip is not within a week.
</p>

<p><strong>Bag Fees and Guidelines at Frontier Airlines</strong></p>
<p>Personal possessions are covered in the basic fare and must fit within the dimensions of 8 x 18 x 14 inches. Carry-on luggage has to be 10 x 16 x 24 inches in size and weigh no more than 35 pounds. Checked luggage should have a total length of 62 linear inches and a weight of 50 pounds or less. Both kinds of luggage have different prices based on when you buy them, so get your bags online ahead of the flight and save money.</p>





