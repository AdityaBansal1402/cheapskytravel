<h2>Top Attractions in New York City</h2>
<p><strong>Statue of Liberty:</strong> The Statue of Liberty was built in 1886 and gifted by France to America. It is one of the greatest American icons today and is widely seen as a world symbol of freedom. Standing at around 152-feet tall from the base to the torch, it is also one of the largest statues in the world. You can have good views of the Statue of Liberty from Battery Park, on the southern tip of Manhattan. Consider taking a short boat trip to Liberty Island to see the statue up close.</p>
<p><strong>Empire State Building:</strong> The Empire State Building is one of the key tourist attractions and a prominent landmark in New York City. The 102-storey, 381-meter-tall building was the world’s tallest until the 1 World Trade Center tower rose higher, 41 years later. Soak up astounding views of the NYC skyline from atop the two observatories.  </p>
<p><strong>Times Square:</strong> The Times Square with its huge, brilliantly lit screens and billboards is the place to go in NYC in the evening. If you however want to visit it during daytime then it remains exciting as ever. The Times Square was formerly called Longacre Square, and got its present name in 1904 after the New York Times tower. Times Square is a fantastic location for NYC's New Year's Eve Celebrations and the famous "ball drop" at midnight.</p>
<p>New York City boasts of diverse landscapes offering an array of attractions and natural wonders. New York is presently home to 262 National Historic Landmarks and 22 national parks. In addition to the top three attractions stated above, other major places of tourist interest worth visiting in NYC include Brooklyn Bridge, Fifth Avenue, Rockefeller Center, Central Park, Broadway, Manhattan, Metropolitan Museum of Art, Broadway, Theater District, 9/11 Memorial and Museum, High Line, Grand Central Terminal, One World Observatory and Guggenheim Museum.</p>
<p>Many annual festivals and cultural events are held in New York round the year. Most popular festivals here include Three Kings Day Parade and Winter Antiques Show (both held in January), St. Patrick’s Day Parade (February), Tribeca Film Festival (April), Rochester International Jazz Festival and River to River Arts Festival (both held in June), Great New York State Fair (August), and Albany Festival of Nations (October).</p>
