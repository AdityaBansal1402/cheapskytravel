            <h3>Introduction to frontier airlines</h3>
            <p>Frontier Airlines, based in Denver, Colorado, is really a low-cost carrier that flies to about 90 locations in the United States, Mexico, the Caribbean, and Central America. If you're a budget traveler, you're certainly aware with Frontier's low fares.</p>
            <p>Aside from the flight schedules and costs, there are a few interesting facts regarding Frontier Airlines. It's dubbed "America's greenest airline" because it saves 43 percent on fuel compared to other American airlines. In addition, every plane has an animal on its tail to raise awareness to these animals and their environments.</p>
            <p>Frontier Airlines is a low-cost carrier, which means that its fares match the conventional low-cost strategy. Frontier offers a basic ticket which includes only your flight and one personal item. Everything else is an extra charge.</p>
             <div class="all-content">
                 
<strong>Frontier provides three tickets options:</strong><br>
<ul>
<li>Basic fare</li>
<li>The Works</li>
<li>The Perks </li>
</ul>
<p>Frontier's basic fare is, as said before, only that: basic. This package includes transportation from place To place in a regular seat with one personal item.</p>
<p>The WORKS and PERKS options from Frontier are "bundles." What exactly is a bundle, you might wonder?</p>
<p>Frontier provides you two tiers of extras that are neatly bundled together and offered at a discounted rate.</p>
<p>Frontier's top-tier bundle seems to be the WORKS. The price starts at $59 one-way and fluctuates depending on the route; it is also dependent on purchasing a round-trip ticket.</p>

                 
            <h3>The Works</h3>
            <p>This package includes various "frills" at a 50 percent discount over adding them a la carte, according to Frontier.</p>
<ul>
<li>The possibility to get your ticket refunded</li>
<li>There are no change fees</li>
<li>Choosing a seat</li>
<li>One carry-on bag and one checked luggage are provided.</li>
<li>Boarding priority</li>
</ul>
       
<h3>The Perks</h3>
