<p>For all sorts of flights, travelers should think about the same things: safety protocols, aircraft cleanliness, seat spacing, and flight time. There are certainly important factors to consider when determining whether to travel international or domestic are external factors, like where you're traveling, infection rates at the destination, measures in place, whether you'll get enough health care, and any travel bans or quarantine requirements.</p>
